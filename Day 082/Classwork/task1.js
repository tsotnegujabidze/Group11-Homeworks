// (ზღვარი), ფუნქციამ 0-იდან border-ის ჩათვლით უნდა გაიგოს ყველა რიცხვი if პირობითი განცხადების 
// საშვალებით, ეს ყველა ლუწი რიცხვი უნდა შეკრიბოთ და დააბრუნოთ

function evenSum(border){
    let sum = 0;
    for(let i = 0; i <= border;i++){
        if(i % 2 === 0){
            sum = sum + i;
        }
    }

    return sum;
}